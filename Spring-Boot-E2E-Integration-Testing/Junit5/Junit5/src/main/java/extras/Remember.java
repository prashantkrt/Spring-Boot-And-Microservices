package extras;


 class MathExample1 {
    public static void main(String[] args) {
        double value1 = 5.7;
        double value2 = -3.7;

        System.out.println("Math.floor(5.7): " + Math.floor(value1));  // Output: 5.0
        System.out.println("Math.floor(-3.7): " + Math.floor(value2)); // Output: -4.0
    }
}

class MathExample2 {
    public static void main(String[] args) {
        double value1 = 5.1;
        double value2 = -3.1;

        System.out.println("Math.ceil(5.1): " + Math.ceil(value1));  // Output: 6.0
        System.out.println("Math.ceil(-3.1): " + Math.ceil(value2)); // Output: -3.0
    }
}

